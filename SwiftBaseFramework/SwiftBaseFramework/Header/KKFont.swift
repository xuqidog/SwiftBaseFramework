import UIKit


/// 字体
func kkFont(_ size: CGFloat) -> UIFont {
    return UIFont.systemFont(ofSize: size)
}
/// 字体粗体
func kkBoldFont(_ size: CGFloat) -> UIFont {
    return UIFont.boldSystemFont(ofSize: size)
}
